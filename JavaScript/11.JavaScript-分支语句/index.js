/***逻辑运算符 */
// > 大于
// < 小于
// = 赋值运算
// == 等于
// === 等于
// || 逻辑或
// && 逻辑与
// != 不等于


const age = 30;

if (age > 10) {
    console.log("年龄大于10 岁");
}

//== 与 === 的区别
// == 只是值之间的比较，=== 是地址之间的比较，是严格等于

if (age == '30') {
    console.log('年龄为30 岁');
}

if (age === '30') {
    console.log("年龄为30 岁");
} else if (age === 30) {
    console.log("年龄的确为30岁");
}

