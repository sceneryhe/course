/**二、console的类型********************************** */

//1. 打印文本log
console.log("Hello world");

//2. 打印数字log
console.log(123);

//3. 打印一个变量
var greeting = "Welcome to our class!";
console.log(greeting);

//4. 打印一个数组
console.log([1,2,3,4]);

//5. 打印一个对象
console.log({a:1,b:2});

//7. 打印一个log table
console.table({a:1,b:2});

//8. 打印error log
console.error("There is an error!");

// 9.清空所有log信息 
console.clear();

// 10. 打印告警信息
console.log("This is is a warning");

/**三、alert函数********************************** */
